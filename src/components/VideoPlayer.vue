<template>
  <div
    class="flex flex-col gap-4 h-screen bg-gray-800 p-4 text-white"
  >
    <h2
      class="text-xl font-bold mb-4 p-2 rounded w-full"
    >
      Preview
    </h2>
    <!-- 影片與字幕區塊 -->
    <div
      class="relative w-full max-h-[400px]"
    >
      <!-- 播放影片 -->
      <video
        v-if="videoUrl"
        :key="videoKey"
        ref="videoRef"
        class="w-full max-h-[400px] rounded"
        controls
        playsinline
      >
        <source :src="videoUrl" type="video/mp4" />
        Your browser does not support the video tag.
      </video>

      <!-- 字幕疊字 -->
      <div
        v-if="currentOverlay"
        class="absolute bottom-4 left-1/2 transform -translate-x-1/2 bg-black bg-opacity-60 text-white text-lg px-4 py-2 rounded max-w-[90%] text-center"
      >
        {{ currentOverlay.text }}
      </div>
    </div>

    <!-- 控制列 -->
    <div
      v-if="videoRef"
      class="flex flex-col gap-2"
    >
      <!-- 播放控制按鈕 -->
      <div
        class="flex items-center justify-center gap-4"
      >
        <button
          class="p-2 text-white hover:text-[#3c82f6] transition-colors"
          @click="seekTo(0)"
        >
          <svg
            class="w-6 h-6"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              stroke-width="2"
              d="M11 19l-7-7 7-7m8 14l-7-7 7-7"
            />
          </svg>
        </button>
        <button
          class="p-2 text-white hover:text-[#3c82f6] transition-colors"
          @click="togglePlay"
        >
          <svg
            v-if="!isPlaying"
            class="w-6 h-6"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              stroke-width="2"
              d="M14.752 11.168l-3.197-2.132A1 1 0 0010 9.87v4.263a1 1 0 001.555.832l3.197-2.132a1 1 0 000-1.664z"
            />
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              stroke-width="2"
              d="M21 12a9 9 0 11-18 0 9 9 0 0118 0z"
            />
          </svg>
          <svg
            v-else
            class="w-6 h-6"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              stroke-width="2"
              d="M10 9v6m4-6v6m7-3a9 9 0 11-18 0 9 9 0 0118 0z"
            />
          </svg>
        </button>
        <button
          class="p-2 text-white hover:text-[#3c82f6] transition-colors"
          @click="seekTo(videoRef.duration)"
        >
          <svg
            class="w-6 h-6"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              stroke-width="2"
              d="M13 5l7 7-7 7M5 5l7 7-7 7"
            />
          </svg>
        </button>
      </div>

      <!-- 時間軸 -->
      <div
        class="relative"
      >
        <div
          class="w-full h-4 bg-[#374151] rounded cursor-pointer"
          @click="handleTimelineClick"
        >
          <!-- Highlight 片段 -->
          <div
            v-for="segment in highlightSegments"
            :key="segment.id"
            class="absolute h-full bg-[#3c82f6]"
            :style="{
              left: `${(segment.start / videoRef.duration) * 100}%`,
              width: `${((segment.end - segment.start) / videoRef.duration) * 100}%`
            }"
          />
          <!-- 當前時間指示器 -->
          <div
            class="absolute top-0 h-full w-0.5 bg-[#dd4c58]"
            :style="{
              left: `${(currentTime / videoRef.duration) * 100}%`
            }"
          />
        </div>
        <!-- 總時長顯示 -->
        <div
          class="absolute right-0 -top-6 text-white text-sm"
        >
          {{ formatTime(videoRef.duration) }}
        </div>
      </div>
    </div>

    <!-- 上傳影片 -->
    <div
      class="flex flex-col gap-2"
    >
      <input
        ref="fileInput"
        type="file"
        accept="video/*"
        @change="onFileChange"
        class="hidden"
      />
      <button
        @click="$refs.fileInput?.click()"
        class="px-4 py-2 bg-blue-600 text-white rounded hover:bg-blue-700 transition-colors w-fit"
      >
        選擇影片
      </button>
      <div
        v-if="fileName"
        class="text-sm text-gray-300"
      >
        已選擇檔案：{{ fileName }}
      </div>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { ref, watch, nextTick, onMounted, onUnmounted, defineOptions, computed } from 'vue'
import { useTranscriptStore } from '../store'
import { storeToRefs } from 'pinia'

// refs
const videoRef = ref<HTMLVideoElement | null>(null)
const fileInput = ref<HTMLInputElement | null>(null)
const videoUrl = ref<string>('')
const fileName = ref<string>('')
const videoKey = ref<number>(0) // 用來強制重新渲染 video 元素
const currentTime = ref<number>(0)
const isPlaying = ref<boolean>(false)

const store = useTranscriptStore()
const { transcript } = storeToRefs(store)

// 計算所有被選中的片段
const highlightSegments = computed(() => {
  return transcript.value.filter(item => item.highlight)
})

// 格式化時間
function formatTime(seconds: number): string {
  if (!seconds) return '00:00'
  const hours = Math.floor(seconds / 3600)
  const minutes = Math.floor((seconds % 3600) / 60)
  const remainingSeconds = Math.floor(seconds % 60)

  const parts = []
  if (hours > 0) {
    parts.push(hours.toString().padStart(2, '0'))
  }
  parts.push(minutes.toString().padStart(2, '0'))
  parts.push(remainingSeconds.toString().padStart(2, '0'))

  return parts.join(':')
}

// 切換播放/暫停
const togglePlay = () => {
  if (!videoRef.value) return
  
  if (videoRef.value.paused) {
    videoRef.value.play()
    isPlaying.value = true
  } else {
    videoRef.value.pause()
    isPlaying.value = false
  }
}

// 跳轉到指定時間
const seekTo = (time: number) => {
  if (videoRef.value) {
    videoRef.value.currentTime = time
    if (time === videoRef.value.duration) {
      videoRef.value.currentTime = highlightSegments.value[highlightSegments.value.length - 1].end
      videoRef.value.pause()
      isPlaying.value = false
    }
    videoRef.value.play()
  }
}

defineOptions({
  name: 'VideoPlayer'
})

const emit = defineEmits(['timeupdate', 'fileSelected'])

// 監聽 currentTime 的變化
watch(currentTime, (newTime) => {
  emit('timeupdate', newTime)
})

defineExpose({
  seekTo
})

const currentOverlay = ref<{ start: number; end: number; text: string } | null>(null)

let animationFrameId: number

// 上傳影片並取得 URL
async function onFileChange(e: Event) {
  const input = e.target as HTMLInputElement
  const file = input.files?.[0]
  
  if (file) {
    try {
      // 檢查檔案類型
      if (!file.type.startsWith('video/')) {
        throw new Error('請上傳影片檔案')
      }
      
      // 創建新的 URL
      videoUrl.value = URL.createObjectURL(file)
      // 設定檔案名稱
      fileName.value = file.name
      
      // 重置 input
      const fileInput = input
      if (fileInput) {
        fileInput.value = '';
      }
      // 強制重新渲染 video 元素
      videoKey.value++
      
      // 載入字幕並取得 highlight 片段
      const res = await fetch(`${import.meta.env.BASE_URL}mock/transcript.json`)
      const data = await res.json()
      store.setTranscript(data)
      
      // 發出文件選擇事件
      emit('fileSelected', file)
    } catch (error) {
      console.error('❌ 影片無法播放，請確認格式或重新上傳')
    }
  }
}

// 字幕即時更新
function updateSubtitle() {
  if (videoRef.value) {
    const currentTime = videoRef.value.currentTime
    const match = transcript.value.find(
      (item) => currentTime >= item.start && currentTime <= item.end
    )
    currentOverlay.value = match || null
  }
  animationFrameId = requestAnimationFrame(updateSubtitle)
}

// 處理 highlight 片段的跳轉邏輯
function handleHighlightSegmentJump(time: number) {
  if (!videoRef.value) return

  // 檢查當前時間是否在任何 highlight 片段內
  const isInHighlight = highlightSegments.value.some(
    (segment) => time >= segment.start && time <= segment.end
  )
  
  // 如果不在 highlight 片段內，找下一個 highlight 片段
  if (!isInHighlight) {
    const nextSegment = highlightSegments.value.find(
      (segment) => segment.start > time
    )
    
    if (nextSegment) {
      videoRef.value.currentTime = nextSegment.start
    } else {
      // 如果沒有下一個 highlight 片段，表示已經播放完最後一個片段
      videoRef.value.currentTime = highlightSegments.value[highlightSegments.value.length - 1].end
      videoRef.value.pause()
      isPlaying.value = false
    }
  }
}

// 當影片路徑改變，開始追蹤播放進度
watch(videoUrl, async (newUrl) => {
  if (!newUrl) return
  await nextTick()
  if (videoRef.value) {
    // 重新綁定事件監聽器
    videoRef.value.addEventListener('error', onVideoError)
    videoRef.value.addEventListener('loadeddata', () => {
      console.log('✅ 影片成功載入')
      console.log('🎬 開始追蹤字幕...')
      cancelAnimationFrame(animationFrameId) // reset old loop
      animationFrameId = requestAnimationFrame(updateSubtitle)
    })
    
    // 監聽時間更新
    videoRef.value.addEventListener('timeupdate', () => {
      if (videoRef.value) {
        const time = videoRef.value.currentTime
        currentTime.value = time
        handleHighlightSegmentJump(time)
      }
    })
    
    // 監聽播放狀態變化
    videoRef.value.addEventListener('play', () => {
      isPlaying.value = true
    })
    videoRef.value.addEventListener('pause', () => {
      isPlaying.value = false
    })
    videoRef.value.addEventListener('ended', () => {
      isPlaying.value = false
    })
  }
})

// 錯誤處理
function onVideoError() {
  console.error('❌ 影片無法播放，請確認格式或重新上傳')
}

// 清除動畫 frame 和 URL
onUnmounted(() => {
  cancelAnimationFrame(animationFrameId)
  if (videoUrl.value.startsWith('blob:')) {
    URL.revokeObjectURL(videoUrl.value)
  }
})

watch(transcript, () => {
  // 當 transcript 更新時，重新檢查當前時間是否在 highlight 片段內
  if (videoRef.value) {
    handleHighlightSegmentJump(videoRef.value.currentTime)
  }
}, { deep: true })

// 處理時間軸點擊
function handleTimelineClick(event: MouseEvent) {
  if (!videoRef.value) return
  
  const currentPosition = (event.currentTarget as HTMLElement).getBoundingClientRect()
  const clickPosition = event.clientX
  const percentage = (clickPosition - currentPosition.left) / currentPosition.width
  const clickTime = percentage * videoRef.value.duration
  
  videoRef.value.currentTime = clickTime
}
</script>

<style scoped>
video {
  box-shadow: 0 0 10px rgba(0, 0, 0, 0.2);
}
</style>
